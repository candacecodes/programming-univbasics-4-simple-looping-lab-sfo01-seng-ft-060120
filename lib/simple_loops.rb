def loop_message_five_times(string)
counter = 0
 
 while counter < 5
   puts "Hello World.\n"
   counter += 1
 end
end

def loop_message_n_times(string)
  counter = 0 
for counter in 0..5 do
  puts "Hello Moon.\n"
  counter += 1
for counter in 0..10 do
  puts "Hello Red Balloon.\n"
  counter += 1
end
